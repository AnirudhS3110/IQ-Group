export function getBlogPostBySlug(slug) {
  return blogPosts.find(post => post.id === slug) || null;
}

export function getAllBlogPostSlugs() {
  return blogPosts.map(post => post.id);
}

export function getRelatedPosts(currentPostId, count = 3) {
  return blogPosts
    .filter(post => post.id !== currentPostId)
    .slice(0, count)
    .map(post => ({
      type: post.type,
      date: post.date,
      title: post.title,
      description: post.subtitle,
      image: post.heroImage,
      link: `/resources/insights/${post.id}`
    }));
}
